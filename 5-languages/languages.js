const lang = '';

switch (lang) {
    case 'ru':
        console.log('Добрый день!');
        break;
    case 'de':
        console.log('Guten Tag!');
        break;
    case 'it':
        console.log('Buon giorno!');
        break;
    case 'fr':
        console.log('Bonjour!');
        break;
    case 'en':
        console.log('Hello');
        break;
    default:
        console.log('Неизвестный язык');
}